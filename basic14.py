#continue example
for letter in 'Python':
   if letter == 'h':
      continue
      print ('This is pass block')
   print ('Current Letter :', letter)

print ("continue example")

#break example
for letter in 'Python':
   if letter == 'h':
      break
      print ('This is pass block')
   print ('Current Letter :', letter)

print ("break example")

#pass example
for letter in 'Python':
   if letter == 'h':
      pass
      print ('This is pass block')
   print ('Current Letter :', letter)

print ("pass example")

